let currentLang = localStorage.getItem('film-lang') || 'de';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('film-lang', lang);
}

function t(key, vars = {}) {
  let str = I18N[currentLang]?.[key] || I18N.de[key] || key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replace(`{${k}}`, v);
  }
  return str;
}

function tmdbLang() {
  return currentLang === 'en' ? 'en-US' : 'de-DE';
}
